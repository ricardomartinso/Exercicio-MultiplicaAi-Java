public class MultiplicaAi {
    public static void main(String[] args) {
        final int numberToMultiply = 9;

        System.out.println("Tabela de multiplicação do 9 :");

        for (int i = 1; i < 11; i++) {
            System.out.println("9 * " + i + " = " + numberToMultiply * i);
        }
    }
}
